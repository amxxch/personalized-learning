package com.ttp.learning_web.learningPlatform.dto;

public class ExerciseStats {
    private Integer totalExercises;
    private Integer easyExercises;
    private Integer mediumExercises;
    private Integer hardExercises;

    public Integer getTotalExercises() {
        return totalExercises;
    }

    public void setTotalExercises(Integer totalExercises) {
        this.totalExercises = totalExercises;
    }

    public Integer getEasyExercises() {
        return easyExercises;
    }

    public void setEasyExercises(Integer easyExercises) {
        this.easyExercises = easyExercises;
    }

    public Integer getMediumExercises() {
        return mediumExercises;
    }

    public void setMediumExercises(Integer mediumExercises) {
        this.mediumExercises = mediumExercises;
    }

    public Integer getHardExercises() {
        return hardExercises;
    }

    public void setHardExercises(Integer hardExercises) {
        this.hardExercises = hardExercises;
    }
}
